// Helper functions
export const getRecipesByCategory = (category: RecipeCategory): Recipe[] => {
  return allRecipes.filter((r) => r.category === category);
};

export const getRecipesByDifficulty = (difficulty: Difficulty): Recipe[] => {
  return allRecipes.filter((r) => r.difficulty === difficulty);
};

export const getPickyEaterFriendlyRecipes = (): Recipe[] => {
  return allRecipes.filter((r) => r.pickyEaterFriendly);
};

export const getRecipesByTag = (tag: string): Recipe[] => {
  return allRecipes.filter((r) => r.tags.includes(tag));
};

export const getRecipeById = (id: string): Recipe | undefined => {
  return allRecipes.find((r) => r.id === id);
};

export const getRecipesBySupermarket = (supermarket: 'rewe' | 'edeka' | 'aldi' | 'lidl'): Recipe[] => {
  return allRecipes.filter((recipe) =>
    recipe.ingredients.some((ing) => ing.supermarket === supermarket)
  );
};
